    }
    
    public function verify($id){
        $user = User::find($id);
        $user->email_verified_at = date('Y-m-d H:i:s');
        $user->save();
        
        // return unverify url
        return route('users.unverify', $id);
    }
    
    public function unverify($id){
        $user = User::find($id);
        $user->email_verified_at = null;
        $user->save();
        //return verify url
        return route('users.verify', $id);
    }
